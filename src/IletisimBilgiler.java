    private void guncelle_butonuActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_guncelle_butonuActionPerformed
    {//GEN-HEADEREND:event_guncelle_butonuActionPerformed
        DefaultTableModel model;
        model = (DefaultTableModel) bilgiTablosu.getModel();

        String yeni_tel = tfiletisim_telNo.getText();
        String yeniSehir = cbiletisim_sehir.getSelectedItem().toString();

        
        Customer.anasayfa.getInmusteri().setTel_no(yeni_tel);
        Customer.anasayfa.getInmusteri().setSehir(yeniSehir);
        model.setValueAt( Customer.anasayfa.getInmusteri().getSehir(), 0, 0);
        model.setValueAt( Customer.anasayfa.getInmusteri().getTel_no(), 0, 2);

    }//GEN-LAST:event_guncelle_butonuActionPerformed

