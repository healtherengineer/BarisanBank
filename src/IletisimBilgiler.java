    private void aktarButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_aktarButtonActionPerformed
    {//GEN-HEADEREND:event_aktarButtonActionPerformed
        DefaultTableModel model;
        model = (DefaultTableModel) bilgiTablosu.getModel();
        Object[] eklenecek =
        {
            Customer.anasayfa.getInmusteri().getSehir(),  Customer.anasayfa.getInmusteri().getKullaniciAdi(),  Customer.anasayfa.getInmusteri().getTel_no()
        };

        model.addRow(eklenecek);
        JOptionPane.showMessageDialog(this, "Bilgileriniz başarıyla aktarıldı...");
    }//GEN-LAST:event_aktarButtonActionPerformed

